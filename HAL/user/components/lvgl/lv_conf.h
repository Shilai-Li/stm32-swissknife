/**
 * @file lv_conf.h
 * @brief LVGL Configuration for STM32 HAL (Bare Metal)
 * @version 9.4.0
 *
 * ============================================================================
 *                          CUBEMX CONFIGURATION GUIDE
 * ============================================================================
 *
 * 1. SYSTEM TIMER (for lv_tick):
 *    - SYS -> Timebase Source: SysTick (default)
 *    - Or use a separate timer (TIM6/TIM7) for LVGL tick
 *
 * 2. DISPLAY INTERFACE - Choose ONE:
 *
 *    A) SPI Display (ST7789, ILI9341, etc.):
 *       - Enable SPIx (e.g., SPI1)
 *       - Mode: Transmit Only Master
 *       - Data Size: 8 Bits
 *       - Prescaler: Adjust for ~20MHz or higher
 *       - GPIO: Configure CS, DC, RST as Output
 *
 *    B) I2C Display (SSD1306, etc.):
 *       - Enable I2Cx (e.g., I2C1)
 *       - Speed Mode: Fast Mode (400kHz)
 *
 *    C) Parallel Display (8080/6800):
 *       - Configure FSMC/FMC or GPIO bit-banging
 *
 * 3. INPUT DEVICE (Optional):
 *    - Touch: Configure I2C/SPI for touch controller
 *    - Encoder: Configure TIM in Encoder Mode
 *    - Buttons: Configure GPIO inputs
 *
 * 4. DMA (Optional, for performance):
 *    - Enable DMA for SPI TX
 *
 * ============================================================================
 *                          MEMORY REQUIREMENTS
 * ============================================================================
 *
 * Minimum RAM: ~16KB (with small display buffer)
 * Recommended RAM: ~32KB+ for smooth operation
 *
 * Display Buffer Sizes:
 * - 128x64 (SSD1306): ~1KB minimum
 * - 240x240 (ST7789): ~9.6KB minimum (1/5 screen)
 * - 320x240 (ILI9341): ~12.8KB minimum (1/5 screen)
 *
 * ============================================================================
 */

